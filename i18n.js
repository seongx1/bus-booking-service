// Current language (default: English)
let currentLang = 'en';

// Get nested translation value
function getTranslation(key, lang) {
    const keys = key.split('.');
    let value = translations[lang];
    
    for (const k of keys) {
        if (value && typeof value === 'object' && k in value) {
            value = value[k];
        } else {
            return null;
        }
    }
    
    return typeof value === 'string' ? value : null;
}

// Update page content with translations
function updateContent(lang) {
    currentLang = lang;
    document.documentElement.lang = lang;
    
    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        const translation = getTranslation(key, lang);
        
        if (translation) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                // Don't update input/textarea content, only placeholders
                return;
            } else {
                element.textContent = translation;
            }
        }
    });
    
    // Update placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        const translation = getTranslation(key, lang);
        
        if (translation) {
            element.placeholder = translation;
        }
    });
    
    // Force English locale for date inputs
    document.querySelectorAll('input[type="date"]').forEach(dateInput => {
        dateInput.setAttribute('lang', 'en');
        // Force English format by setting value format
        if (dateInput.value) {
            // Keep the value but ensure it's in English format
            const dateValue = dateInput.value;
            dateInput.value = '';
            setTimeout(() => {
                dateInput.value = dateValue;
            }, 0);
        }
    });
    
    // Update language dropdown
    const langToggle = document.getElementById('langToggle');
    const langDropdown = document.getElementById('langDropdown');
    const langCurrent = document.querySelector('.lang-current');
    
    if (langCurrent) {
        const currentText = getTranslation('lang.current', lang);
        if (currentText) {
            langCurrent.textContent = currentText;
        }
    }
    
    // Update language options
    document.querySelectorAll('.lang-option').forEach(option => {
        const optionLang = option.getAttribute('data-lang');
        option.removeAttribute('aria-current');
        
        if (optionLang === lang) {
            option.setAttribute('aria-current', 'true');
        }
        
        // Update subtitles
        const subtitle = option.querySelector('.lang-subtitle');
        if (subtitle) {
            const subtitleKey = `lang.${optionLang}.subtitle`;
            const subtitleText = getTranslation(subtitleKey, lang);
            if (subtitleText) {
                subtitle.textContent = subtitleText;
            }
        }
    });
    
    // Close dropdown after language change
    if (langDropdown) {
        langDropdown.classList.remove('active');
        if (langToggle) {
            langToggle.setAttribute('aria-expanded', 'false');
        }
    }
    
    // Save language preference
    localStorage.setItem('preferredLanguage', lang);
}

// Language dropdown toggle
function toggleLanguageDropdown() {
    const langToggle = document.getElementById('langToggle');
    const langDropdown = document.getElementById('langDropdown');
    
    if (!langToggle || !langDropdown) return;
    
    const isExpanded = langToggle.getAttribute('aria-expanded') === 'true';
    
    if (isExpanded) {
        langDropdown.classList.remove('active');
        langToggle.setAttribute('aria-expanded', 'false');
    } else {
        langDropdown.classList.add('active');
        langToggle.setAttribute('aria-expanded', 'true');
    }
}

// Close dropdown when clicking outside
function closeLanguageDropdown(event) {
    const langToggle = document.getElementById('langToggle');
    const langDropdown = document.getElementById('langDropdown');
    
    if (!langToggle || !langDropdown) return;
    
    if (!langToggle.contains(event.target) && !langDropdown.contains(event.target)) {
        langDropdown.classList.remove('active');
        langToggle.setAttribute('aria-expanded', 'false');
    }
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', () => {
    // Get saved language preference or default to English
    const savedLang = localStorage.getItem('preferredLanguage') || 'en';
    updateContent(savedLang);
    
    // Language dropdown toggle
    const langToggle = document.getElementById('langToggle');
    const langDropdown = document.getElementById('langDropdown');
    
    if (langToggle) {
        langToggle.addEventListener('click', (e) => {
            e.stopPropagation();
            toggleLanguageDropdown();
        });
    }
    
    // Language option selection
    if (langDropdown) {
        document.querySelectorAll('.lang-option').forEach(option => {
            option.addEventListener('click', (e) => {
                e.stopPropagation();
                const lang = option.getAttribute('data-lang');
                updateContent(lang);
            });
        });
    }
    
    // Close dropdown when clicking outside
    document.addEventListener('click', closeLanguageDropdown);
    
    // Keyboard navigation for dropdown
    if (langToggle) {
        langToggle.addEventListener('keydown', (e) => {
            if (e.key === 'Enter' || e.key === ' ') {
                e.preventDefault();
                toggleLanguageDropdown();
            } else if (e.key === 'Escape') {
                const langDropdown = document.getElementById('langDropdown');
                if (langDropdown && langDropdown.classList.contains('active')) {
                    langDropdown.classList.remove('active');
                    langToggle.setAttribute('aria-expanded', 'false');
                }
            }
        });
    }
    
    // Keyboard navigation for options
    document.querySelectorAll('.lang-option').forEach(option => {
        option.addEventListener('keydown', (e) => {
            if (e.key === 'Enter' || e.key === ' ') {
                e.preventDefault();
                const lang = option.getAttribute('data-lang');
                updateContent(lang);
            }
        });
    });
});

